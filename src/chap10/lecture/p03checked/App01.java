		// : RuntimeException 클래스 상속 받지 않은 모든 Exception 타입의 객체
		
		
		// 실행예외(Runtime Exception, unchecked Exception)
		// : 컴파일러가 예외처리했는지 검사 안함(unchecked)
		//   예외처리 안해도 컴파일 오류 아님
		// : RuntimeException 클래스 상속 받은 모든 Exception 타입의 객체
		
		
